// טוען את חבילת Express מה־node_modules כדי להשתמש בשרת HTTP ופיצ'רים של ראוטינג
const express = require('express');

// יוצר מופע של אפליקציית Express (האובייקט המרכזי שמנהל את השרת והראוטים)
const app = express();


// -------------------- Routes (נתיבים) --------------------

// מגדיר ראוט GET לנתיב השורש '/'.
// req הוא האובייקט של הבקשה מהלקוח, res הוא האובייקט של התשובה שנחזיר.
// בפנים שולחים טקסט פשוט כתגובה.
app.get('/', (req, res) => {
  res.send('Task Manger App'); // טיפ: עדיף "Manager" :)
});


// -------------------- Server (שרת) --------------------

// מגדיר את הפורט שעליו השרת יאזין (3000 מקומי).
const port = 3000;

// מפעיל את השרת להאזין על הפורט שנקבע.
// הפרמטר השני הוא פונקציית callback שרק מדפיסה שהשרת רץ בהצלחה.
app.listen(port, () => console.log(`Server running on port ${port}`));
